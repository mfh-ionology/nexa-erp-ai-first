// updateJournalEntry (AC-2, AC-9)
// ---------------------------------------------------------------------------

export async function updateJournalEntry(
  prisma: PrismaClient,
  _eventBus: EventBus,
  companyId: string,
  id: string,
  data: UpdateJournalInput,
  userId: string,
) {
  const entry = await prisma.$transaction(async (tx) => {
    // Fetch existing entry
    const existing = await tx.journalEntry.findFirst({
      where: { id, companyId },
      select: { id: true, status: true, source: true, transactionDate: true },
    });

    if (!existing) {
      throw new NotFoundError('NOT_FOUND', 'Journal entry not found');
    }

    // AC-9, BR-FIN-011: Cannot modify POSTED or REVERSED entry
    if (existing.status !== 'DRAFT') {
      throw new DomainError(
        'ENTRY_IMMUTABLE',
        `Cannot modify a ${existing.status} journal entry (BR-FIN-011)`,
      );
    }

    // Validate period if being changed
    if (data.periodId) {
      const period = await tx.financialPeriod.findFirst({
        where: { id: data.periodId, companyId },
        select: { id: true },
      });
      if (!period) {
        throw new NotFoundError('PERIOD_NOT_FOUND', 'Financial period not found');
      }
    }

    // If lines are being replaced, validate accounts, convert currencies, and rebuild
    let convertedLines: Array<{
      accountCode: string;
      description?: string | null;
      debit: number;
      credit: number;
      vatCode?: string | null;
      currencyCode: string | null;
      foreignAmount: number | null;
      exchangeRate: number | null;
      dimensions?: Array<{ dimensionValueId: string }>;
    }> | null = null;

    if (data.lines) {
      const accountCodes = data.lines.map((l) => l.accountCode);
      const uniqueCodes = [...new Set(accountCodes)];
      const accounts = await tx.chartOfAccount.findMany({
        where: { companyId, code: { in: uniqueCodes } },
        select: { code: true },
      });
      const foundCodes = new Set(accounts.map((a) => a.code));
      for (const code of uniqueCodes) {
        if (!foundCodes.has(code)) {
          throw new AppError('ACCOUNT_NOT_FOUND', `Account code "${code}" not found`, 400);
        }
      }

      // Multi-currency conversion (E14-S20)
      const txDate = data.transactionDate ?? existing.transactionDate ?? new Date();
      const baseCurrencyCode = await getBaseCurrencyCode(tx, companyId);
      convertedLines = await convertLinesToBaseCurrency(
        tx,
        companyId,
        baseCurrencyCode,
        txDate instanceof Date ? txDate : new Date(txDate),
        data.lines.map((l) => ({
          ...l,
          debit: l.debit ?? 0,
          credit: l.credit ?? 0,
        })),
      );

      // Delete existing lines (cascade deletes dimensions)
      await tx.journalLine.deleteMany({
        where: { journalEntryId: id },
      });

      // Create new lines with converted amounts
      for (let i = 0; i < convertedLines.length; i++) {
        const line = convertedLines[i]!;
        const originalLine = data.lines[i]!;
        const createdLine = await tx.journalLine.create({
          data: {
            journalEntryId: id,
            lineNumber: i + 1,
            accountCode: line.accountCode,
            companyId,
            description: line.description ?? null,
            debit: line.debit,
            credit: line.credit,
            vatCode: line.vatCode ?? null,
            currencyCode: line.currencyCode ?? null,
            foreignAmount: line.foreignAmount ?? null,
            exchangeRate: line.exchangeRate ?? null,
          },
          select: { id: true },
        });

        if (originalLine.dimensions && originalLine.dimensions.length > 0) {
          for (const dim of originalLine.dimensions) {
            await tx.journalLineDimension.create({
              data: {
                journalLineId: createdLine.id,
                dimensionValueId: dim.dimensionValueId,
              },
            });
          }
        }
      }
    }

    // Calculate new totals if lines changed (using converted base-currency amounts)
    const updateData: Record<string, unknown> = { updatedBy: userId };
    if (data.transactionDate !== undefined) updateData.transactionDate = data.transactionDate;
    if (data.description !== undefined) updateData.description = data.description;
    if (data.reference !== undefined) updateData.reference = data.reference;
    if (data.periodId !== undefined) updateData.periodId = data.periodId;

    if (convertedLines) {
      updateData.totalDebit = convertedLines.reduce((sum, l) => sum + l.debit, 0);
      updateData.totalCredit = convertedLines.reduce((sum, l) => sum + l.credit, 0);
    }

    await tx.journalEntry.update({
      where: { id },
      data: updateData,
    });

    // Fetch the complete updated entry
    return tx.journalEntry.findUniqueOrThrow({
      where: { id },
      select: DETAIL_SELECT,
    });
  });

  return normaliseDetail(entry as unknown as Record<string, unknown>);
}

// ---------------------------------------------------------------------------
// postJournalEntry (AC-3, AC-4, AC-5, AC-6, AC-8, AC-12, AC-13)
// ---------------------------------------------------------------------------

export async function postJournalEntry(
  prisma: PrismaClient,
  eventBus: EventBus,
  companyId: string,
  id: string,
  userId: string,
) {
  const entry = await prisma.$transaction(async (tx) => {
    // Fetch the entry with lines and dimensions
    const existing = await tx.journalEntry.findFirst({
      where: { id, companyId },
      select: {
        id: true,
        entryNumber: true,
        status: true,
        source: true,
        periodId: true,
        transactionDate: true,
        lines: {
          select: {
            accountCode: true,
            debit: true,
            credit: true,
            dimensions: { select: { dimensionValueId: true } },
          },
        },
      },
    });

    if (!existing) {
      throw new NotFoundError('NOT_FOUND', 'Journal entry not found');
    }

    // Only DRAFT can be posted
    if (existing.status !== 'DRAFT') {
      throw new DomainError(
        'INVALID_STATUS_TRANSITION',
        `Cannot post a ${existing.status} entry. Only DRAFT entries can be posted (BR-FIN-011)`,
      );
    }

    // Normalise line amounts for validation
    const lines = existing.lines.map((l) => ({
      accountCode: l.accountCode,
      debit: toNumber(l.debit as unknown as Prisma.Decimal),
      credit: toNumber(l.credit as unknown as Prisma.Decimal),
      dimensions: l.dimensions.map((d) => ({ dimensionValueId: d.dimensionValueId })),
    }));

    // BR-FIN-001: debits must equal credits
    const totalDebit = lines.reduce((sum, l) => sum + l.debit, 0);
    const totalCredit = lines.reduce((sum, l) => sum + l.credit, 0);
    // Use a small epsilon for floating-point comparison
    if (Math.abs(totalDebit - totalCredit) > 0.0001) {
      throw new DomainError(
        'ENTRY_NOT_BALANCED',
        `Journal entry is not balanced: debits (${String(totalDebit)}) != credits (${String(totalCredit)}) (BR-FIN-001)`,
      );
    }

    // AC-8, BR-FIN-003: Cannot post to CLOSED or LOCKED period
    await validatePeriodForPosting(tx, companyId, existing.periodId);

    // Validate accounts: active, postable, control account check for MANUAL (AC-13)
    const isManual = existing.source === 'MANUAL';
    await validateAccountsForPosting(
      tx,
      companyId,
      lines.map((l) => l.accountCode),
      isManual,
    );

    // AC-12: Enforce DimensionRequirement rules
    await validateDimensionRequirements(tx, companyId, lines);

    // BR-FIN-DIM-001: Single-select dimension constraint
    await validateSingleSelectDimensions(tx, lines);

    // Transition to POSTED (AC-3)
    const posted = await tx.journalEntry.update({
      where: { id },
      data: {
        status: 'POSTED',
        postedAt: new Date(),
        postedBy: userId,
        totalDebit,
        totalCredit,
      },
      select: DETAIL_SELECT,
    });

    // AC-4: Update account balances
    await updateAccountBalances(tx, companyId, lines);

    // AC-5: Update dimension balances
    await updateDimensionBalances(tx, companyId, existing.periodId, lines);

    return posted;
  });

  // AC-6: Emit journal.posted event
  eventBus.emit('journal.posted', {
    journalEntryId: entry.id,
    entryNumber: entry.entryNumber,
    source: entry.source,
    sourceId: entry.sourceId ?? undefined,
    sourceReference: entry.sourceReference ?? undefined,
    transactionDate: toDateString(entry.transactionDate),
    periodId: entry.periodId,
    totalAmount: String(toNumber(entry.totalDebit as unknown as Prisma.Decimal)),
    lineCount: entry.lines.length,
    createdBy: userId,
  });

  return normaliseDetail(entry as unknown as Record<string, unknown>);
}

// ---------------------------------------------------------------------------
// reverseJournalEntry (AC-7)
// ---------------------------------------------------------------------------

export async function reverseJournalEntry(
  prisma: PrismaClient,
  eventBus: EventBus,
  companyId: string,
  id: string,
  userId: string,
) {
  const { original, reversal } = await prisma.$transaction(async (tx) => {
    // Fetch the original entry with lines
    const existing = await tx.journalEntry.findFirst({
      where: { id, companyId },
      select: {
        id: true,
        entryNumber: true,
        status: true,
        source: true,
        sourceId: true,
        sourceReference: true,
        transactionDate: true,
        description: true,
        reference: true,
        periodId: true,
        lines: {
          select: {
            accountCode: true,
            description: true,
            debit: true,
            credit: true,
            vatCode: true,
            currencyCode: true,
            foreignAmount: true,
            exchangeRate: true,
            dimensions: { select: { dimensionValueId: true } },
          },
          orderBy: { lineNumber: 'asc' },
        },
      },
    });

    if (!existing) {
      throw new NotFoundError('NOT_FOUND', 'Journal entry not found');
    }

    // Only POSTED entries can be reversed
    if (existing.status !== 'POSTED') {
      throw new DomainError(
        'INVALID_STATUS_TRANSITION',
        `Cannot reverse a ${existing.status} entry. Only POSTED entries can be reversed (BR-FIN-011)`,
      );
    }

    // Validate period is open
    await validatePeriodForPosting(tx, companyId, existing.periodId);

    // Generate new entry number for the reversal
    const reversalEntryNumber = await nextNumber(tx, companyId, 'JOURNAL');

    // Normalise lines for processing
    const originalLines = existing.lines.map((l) => ({
      accountCode: l.accountCode,
      description: l.description,
      debit: toNumber(l.debit as unknown as Prisma.Decimal),
      credit: toNumber(l.credit as unknown as Prisma.Decimal),
      vatCode: l.vatCode,
      currencyCode: l.currencyCode,
      foreignAmount: l.foreignAmount
        ? toNumber(l.foreignAmount as unknown as Prisma.Decimal)
        : null,
      exchangeRate: l.exchangeRate ? toNumber(l.exchangeRate as unknown as Prisma.Decimal) : null,
      dimensions: l.dimensions,
    }));

    // Create reversal entry with negated amounts (mirror entry)
    const totalDebit = originalLines.reduce((sum, l) => sum + l.credit, 0); // swap debit/credit
    const totalCredit = originalLines.reduce((sum, l) => sum + l.debit, 0);

    const reversalEntry = await tx.journalEntry.create({
      data: {
        companyId,
        entryNumber: reversalEntryNumber,
        transactionDate: existing.transactionDate,
        description: `Reversal of ${existing.entryNumber}: ${existing.description}`,
        reference: existing.reference,
        source: existing.source,
        sourceId: existing.sourceId,
        sourceReference: existing.sourceReference,
        isAutoGenerated: true,
        status: 'POSTED',
        postedAt: new Date(),
        postedBy: userId,
        reversalOfId: existing.id,
        periodId: existing.periodId,
        totalDebit,
        totalCredit,
        createdBy: userId,
        updatedBy: userId,
      },
      select: { id: true },
    });

    // Create reversal lines (debit ↔ credit swapped)
    const reversalLines: Array<{
      accountCode: string;
      debit: number;
      credit: number;
      dimensions: Array<{ dimensionValueId: string }>;
    }> = [];

    for (let i = 0; i < originalLines.length; i++) {
      const origLine = originalLines[i]!;
      const createdLine = await tx.journalLine.create({
        data: {
          journalEntryId: reversalEntry.id,
          lineNumber: i + 1,
          accountCode: origLine.accountCode,
          companyId,
          description: origLine.description,
          debit: origLine.credit, // Swap: original credit becomes reversal debit
          credit: origLine.debit, // Swap: original debit becomes reversal credit
          vatCode: origLine.vatCode,
          currencyCode: origLine.currencyCode,
          foreignAmount: origLine.foreignAmount ? -origLine.foreignAmount : null,
          exchangeRate: origLine.exchangeRate,
        },
        select: { id: true },
      });

      // Copy dimensions
      if (origLine.dimensions.length > 0) {
        for (const dim of origLine.dimensions) {
          await tx.journalLineDimension.create({
            data: {
              journalLineId: createdLine.id,
              dimensionValueId: dim.dimensionValueId,
            },
          });
        }
      }

      reversalLines.push({
        accountCode: origLine.accountCode,
        debit: origLine.credit,
        credit: origLine.debit,
        dimensions: origLine.dimensions,
      });
    }

    // Mark original as REVERSED
    await tx.journalEntry.update({
      where: { id: existing.id },
      data: { status: 'REVERSED' },
    });

    // Update account balances for the reversal
    await updateAccountBalances(tx, companyId, reversalLines);

    // Update dimension balances for the reversal
    await updateDimensionBalances(tx, companyId, existing.periodId, reversalLines);

    // Fetch the complete reversal entry
    const fullReversal = await tx.journalEntry.findUniqueOrThrow({
      where: { id: reversalEntry.id },
      select: DETAIL_SELECT,
    });

    return { original: existing, reversal: fullReversal };
  });

  // Emit events
  eventBus.emit('journal.reversed', {
    journalEntryId: original.id,
    reversalEntryId: reversal.id,
    originalSource: original.source,
    sourceReference: original.sourceReference ?? undefined,
    createdBy: userId,
  });

  return normaliseDetail(reversal as unknown as Record<string, unknown>);
}

// ---------------------------------------------------------------------------
// createGlPosting (AC-10) — Reusable service for sub-module posting
// ---------------------------------------------------------------------------

/**
 * Reusable GL posting service callable by sub-modules (AR, AP, Stock, Payroll, etc.).
 *
 * When source=MANUAL, creates as DRAFT (caller must post separately).
 * When source is anything else (sub-module), creates directly as POSTED (BR-FIN-018).
 */
export async function createGlPosting(
  prisma: PrismaClient,
  eventBus: EventBus,
  companyId: string,
  input: GlPostingInput,
  userId: string,
) {
  const isManual = input.source === 'MANUAL';

  const entry = await prisma.$transaction(async (tx) => {
    // Determine period from transaction date
    const period = await tx.financialPeriod.findFirst({
      where: {
        companyId,
        startDate: { lte: input.transactionDate },
        endDate: { gte: input.transactionDate },
      },
      select: { id: true, status: true },
      orderBy: { periodNumber: 'asc' },
    });

    if (!period) {
      throw new NotFoundError(
        'PERIOD_NOT_FOUND',
        'No financial period found for the transaction date',
      );
    }

    // For sub-module posting (non-MANUAL), validate period is open
    if (!isManual) {
      if (period.status === 'CLOSED' || period.status === 'LOCKED') {
        throw new DomainError(
          'PERIOD_NOT_OPEN',
          `Cannot post to ${period.status.toLowerCase()} period (BR-FIN-003)`,
        );
      }
    }

    // Multi-currency conversion (E14-S20): convert foreign currency lines to base currency
    const baseCurrencyCode = await getBaseCurrencyCode(tx, companyId);
    const convertedLines = await convertLinesToBaseCurrency(
      tx,
      companyId,
      baseCurrencyCode,
      input.transactionDate,
      input.lines.map((l) => ({
        ...l,
        debit: l.debit ?? 0,
        credit: l.credit ?? 0,
      })),
    );

    // BR-FIN-001: Validate balance (on base currency amounts)
    const totalDebit = convertedLines.reduce((sum, l) => sum + l.debit, 0);
    const totalCredit = convertedLines.reduce((sum, l) => sum + l.credit, 0);
    if (Math.abs(totalDebit - totalCredit) > 0.0001) {
      throw new DomainError(
        'ENTRY_NOT_BALANCED',
        `Journal entry is not balanced: debits (${String(totalDebit)}) != credits (${String(totalCredit)}) (BR-FIN-001)`,
      );
    }

    // Validate accounts
    await validateAccountsForPosting(
      tx,
      companyId,
      convertedLines.map((l) => l.accountCode),
      isManual,
    );

    // Validate dimensions
    await validateDimensionRequirements(tx, companyId, convertedLines);
    await validateSingleSelectDimensions(tx, convertedLines);

    // Generate entry number
    const entryNumber = await nextNumber(tx, companyId, 'JOURNAL');

    // Determine status: DRAFT for manual, POSTED for sub-module (BR-FIN-018)
    const status = isManual ? 'DRAFT' : 'POSTED';
    const now = new Date();

    // Create journal entry
    const journalEntry = await tx.journalEntry.create({
      data: {
        companyId,
        entryNumber,
        transactionDate: input.transactionDate,
        description: input.description,
        reference: input.reference ?? null,
        source: input.source as JournalSourceEnum,
        sourceId: input.sourceId ?? null,
        sourceReference: input.sourceReference ?? null,
        isAutoGenerated: !isManual,
        status,
        postedAt: isManual ? null : now,
        postedBy: isManual ? null : userId,
        periodId: period.id,
        totalDebit,
        totalCredit,
        createdBy: userId,
        updatedBy: userId,
      },
      select: { id: true },
    });

    // Create lines (using converted base-currency amounts)
    for (let i = 0; i < convertedLines.length; i++) {
      const line = convertedLines[i]!;
      const originalLine = input.lines[i]!;
      const createdLine = await tx.journalLine.create({
        data: {
          journalEntryId: journalEntry.id,
          lineNumber: i + 1,
          accountCode: line.accountCode,
          companyId,
          description: line.description ?? null,
          debit: line.debit,
          credit: line.credit,
          vatCode: line.vatCode ?? null,
          currencyCode: line.currencyCode ?? null,
          foreignAmount: line.foreignAmount ?? null,
          exchangeRate: line.exchangeRate ?? null,
        },
        select: { id: true },
      });

      if (originalLine.dimensions && originalLine.dimensions.length > 0) {
        for (const dim of originalLine.dimensions) {
          await tx.journalLineDimension.create({
            data: {
              journalLineId: createdLine.id,
              dimensionValueId: dim.dimensionValueId,
            },
          });
        }
      }
    }

    // If posting directly (sub-module), update balances (using converted amounts)
    if (!isManual) {
      await updateAccountBalances(tx, companyId, convertedLines);
      await updateDimensionBalances(tx, companyId, period.id, convertedLines);
    }

    // Fetch the complete entry
    return tx.journalEntry.findUniqueOrThrow({
      where: { id: journalEntry.id },
      select: DETAIL_SELECT,
    });
  });

  // Emit events
  if (!isManual) {
    eventBus.emit('journal.posted', {
      journalEntryId: entry.id,
      entryNumber: entry.entryNumber,
      source: entry.source,
      sourceId: entry.sourceId ?? undefined,
      sourceReference: entry.sourceReference ?? undefined,
      transactionDate: toDateString(entry.transactionDate),
      periodId: entry.periodId,
      totalAmount: String(toNumber(entry.totalDebit as unknown as Prisma.Decimal)),
      lineCount: entry.lines.length,
      createdBy: userId,
    });
  } else {
    eventBus.emit('journal.created', {
      journalEntryId: entry.id,
      entryNumber: entry.entryNumber,
      companyId,
      source: 'MANUAL',
      createdBy: userId,
    });
  }

  return normaliseDetail(entry as unknown as Record<string, unknown>);
}

