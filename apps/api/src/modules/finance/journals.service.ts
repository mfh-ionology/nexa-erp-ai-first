/**
 * Validate that a period exists, belongs to the company, and is OPEN.
 * Returns the period row. Throws DomainError for CLOSED/LOCKED (BR-FIN-003).
 */
async function validatePeriodForPosting(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  companyId: string,
  periodId: string,
) {
  const period = await tx.financialPeriod.findFirst({
    where: { id: periodId, companyId },
    select: { id: true, status: true, fiscalYear: true, periodNumber: true },
  });

  if (!period) {
    throw new NotFoundError('PERIOD_NOT_FOUND', 'Financial period not found');
  }

  if (period.status === 'CLOSED' || period.status === 'LOCKED') {
    throw new DomainError(
      'PERIOD_NOT_OPEN',
      `Cannot post to ${period.status.toLowerCase()} period (BR-FIN-003)`,
    );
  }

  return period;
}

/**
 * Validate all account codes in lines exist, are active, postable, and not control accounts.
 * BR-FIN-013: Cannot manually post to AR/AP control accounts.
 * BR-FIN-013: Cannot post to non-postable accounts (isPostable=false).
 */
async function validateAccountsForPosting(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  companyId: string,
  accountCodes: string[],
  isManual: boolean,
) {
  const uniqueCodes = [...new Set(accountCodes)];
  const accounts = await tx.chartOfAccount.findMany({
    where: { companyId, code: { in: uniqueCodes } },
    select: { code: true, isActive: true, isPostable: true, isControl: true },
  });

  const foundCodes = new Set(accounts.map((a) => a.code));
  for (const code of uniqueCodes) {
    if (!foundCodes.has(code)) {
      throw new AppError('ACCOUNT_NOT_FOUND', `Account code "${code}" not found`, 400);
    }
  }

  for (const account of accounts) {
    if (!account.isActive) {
      throw new DomainError(
        'ACCOUNT_INACTIVE',
        `Account "${account.code}" is inactive and cannot be posted to`,
      );
    }
    if (!account.isPostable) {
      throw new DomainError(
        'ACCOUNT_NOT_POSTABLE',
        `Account "${account.code}" is not postable (BR-FIN-013)`,
      );
    }
    if (isManual && account.isControl) {
      throw new DomainError(
        'CONTROL_ACCOUNT_MANUAL_POST',
        `Cannot manually post to control account "${account.code}" (BR-FIN-013)`,
      );
    }
  }
}

/**
 * Enforce DimensionRequirement rules on posting (AC-12).
 * For each line, check if the account is in any DimensionRequirement range.
 * If so, the line must have a dimension of that type.
 */
async function validateDimensionRequirements(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  companyId: string,
  lines: Array<{
    accountCode: string;
    dimensions?: Array<{ dimensionValueId: string }>;
  }>,
) {
  // Get all active dimension requirements for this company
  const requirements = await tx.dimensionRequirement.findMany({
    where: { companyId, isActive: true, isRequired: true },
    select: {
      dimensionTypeId: true,
      accountCodeFrom: true,
      accountCodeTo: true,
      dimensionType: { select: { code: true, name: true } },
    },
  });

  if (requirements.length === 0) return;

  // Get dimension value → type mappings for all dimension values in lines
  const allDimValueIds = lines
    .flatMap((l) => (l.dimensions ?? []).map((d) => d.dimensionValueId))
    .filter(Boolean);

  const dimValues =
    allDimValueIds.length > 0
      ? await tx.dimensionValue.findMany({
          where: { id: { in: allDimValueIds } },
          select: { id: true, dimensionTypeId: true },
        })
      : [];

  const dimValueTypeMap = new Map(dimValues.map((v) => [v.id, v.dimensionTypeId]));

  for (const line of lines) {
    for (const req of requirements) {
      // Check if account code falls within the requirement range
      if (line.accountCode >= req.accountCodeFrom && line.accountCode <= req.accountCodeTo) {
        // This account requires a dimension of this type
        const lineDimTypeIds = (line.dimensions ?? [])
          .map((d) => dimValueTypeMap.get(d.dimensionValueId))
          .filter(Boolean);

        if (!lineDimTypeIds.includes(req.dimensionTypeId)) {
          throw new DomainError(
            'DIMENSION_REQUIRED',
            `Account "${line.accountCode}" requires dimension "${req.dimensionType.name}" (${req.dimensionType.code}) (BR-FIN-DIM-002)`,
          );
        }
      }
    }
  }
}

/**
 * Validate single-select constraint (BR-FIN-DIM-001).
 * For each line, check that at most one dimension value per type is assigned.
 */
async function validateSingleSelectDimensions(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  lines: Array<{ dimensions?: Array<{ dimensionValueId: string }> }>,
) {
  const allDimValueIds = lines
    .flatMap((l) => (l.dimensions ?? []).map((d) => d.dimensionValueId))
    .filter(Boolean);

  if (allDimValueIds.length === 0) return;

  const dimValues = await tx.dimensionValue.findMany({
    where: { id: { in: allDimValueIds } },
    select: {
      id: true,
      dimensionTypeId: true,
      dimensionType: { select: { isSingleSelect: true, name: true } },
    },
  });

  const dimValueMap = new Map(dimValues.map((v) => [v.id, v]));

  for (const line of lines) {
    if (!line.dimensions || line.dimensions.length === 0) continue;

    const typeIdCounts = new Map<string, number>();
    for (const dim of line.dimensions) {
      const dv = dimValueMap.get(dim.dimensionValueId);
      if (!dv) {
        throw new AppError(
          'DIMENSION_VALUE_NOT_FOUND',
          `Dimension value "${dim.dimensionValueId}" not found`,
          400,
        );
      }
      if (dv.dimensionType.isSingleSelect) {
        const count = (typeIdCounts.get(dv.dimensionTypeId) ?? 0) + 1;
        typeIdCounts.set(dv.dimensionTypeId, count);
        if (count > 1) {
          throw new DomainError(
            'DIMENSION_SINGLE_SELECT',
            `Dimension "${dv.dimensionType.name}" only allows one value per line (BR-FIN-DIM-001)`,
          );
        }
      }
    }
  }
}

