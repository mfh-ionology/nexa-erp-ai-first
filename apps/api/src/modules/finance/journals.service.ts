// listJournalEntries (AC-11)
// ---------------------------------------------------------------------------

export async function listJournalEntries(
  prisma: PrismaClient,
  companyId: string,
  query: ListJournalsQuery,
) {
  const { cursor, limit, status, source, periodId, dateFrom, dateTo } = query;

  const where: Record<string, unknown> = { companyId };
  if (status !== undefined) where.status = status;
  if (source !== undefined) where.source = source;
  if (periodId !== undefined) where.periodId = periodId;
  if (dateFrom || dateTo) {
    const dateFilter: Record<string, unknown> = {};
    if (dateFrom) dateFilter.gte = dateFrom;
    if (dateTo) dateFilter.lte = dateTo;
    where.transactionDate = dateFilter;
  }

  const [items, total] = await Promise.all([
    prisma.journalEntry.findMany({
      where,
      take: limit + 1,
      ...(cursor ? { skip: 1, cursor: { id: cursor } } : {}),
      orderBy: [{ transactionDate: 'desc' }, { entryNumber: 'desc' }],
      select: LIST_SELECT,
    }),
    prisma.journalEntry.count({ where }),
  ]);

  const hasMore = items.length > limit;
  const data = hasMore ? items.slice(0, -1) : items;
  const nextCursor = hasMore ? data[data.length - 1]?.id : undefined;

  const mapped = data.map((row) => normaliseListItem(row as unknown as Record<string, unknown>));

  const meta: PaginationMeta = { cursor: nextCursor, hasMore, total };

  return { data: mapped, meta };
}

