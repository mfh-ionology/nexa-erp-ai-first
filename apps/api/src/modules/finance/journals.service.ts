/**
 * Update ChartOfAccount.currentBalance for each affected account (AC-4).
 */
async function updateAccountBalances(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  companyId: string,
  lines: Array<{ accountCode: string; debit: number; credit: number }>,
) {
  // Group by account code and compute net change
  const balanceChanges = new Map<string, number>();
  for (const line of lines) {
    const current = balanceChanges.get(line.accountCode) ?? 0;
    // debit increases, credit decreases (for assets/expenses; reversed for liabilities/equity/revenue)
    // We store raw debit-credit delta; the currentBalance field accumulates this
    balanceChanges.set(line.accountCode, current + line.debit - line.credit);
  }

  for (const [accountCode, delta] of balanceChanges) {
    if (delta === 0) continue;
    await tx.chartOfAccount.update({
      where: {
        uq_chart_of_account_company_code: { companyId, code: accountCode },
      },
      data: {
        currentBalance: { increment: delta },
      },
    });
  }
}

/**
 * Update DimensionBalance for each JournalLineDimension (AC-5, BR-FIN-DIM-007).
 * Upserts the balance row — creates if not exists, increments if exists.
 */
async function updateDimensionBalances(
  tx: Parameters<Parameters<PrismaClient['$transaction']>[0]>[0],
  companyId: string,
  periodId: string,
  lines: Array<{
    accountCode: string;
    debit: number;
    credit: number;
    dimensions?: Array<{ dimensionValueId: string }>;
  }>,
) {
  // Collect all dimension value IDs to look up their types
  const allDimValueIds = lines
    .flatMap((l) => (l.dimensions ?? []).map((d) => d.dimensionValueId))
    .filter(Boolean);

  if (allDimValueIds.length === 0) return;

  const dimValues = await tx.dimensionValue.findMany({
    where: { id: { in: [...new Set(allDimValueIds)] } },
    select: { id: true, dimensionTypeId: true },
  });

  const dimValueTypeMap = new Map(dimValues.map((v) => [v.id, v.dimensionTypeId]));

  for (const line of lines) {
    if (!line.dimensions || line.dimensions.length === 0) continue;

    for (const dim of line.dimensions) {
      const dimensionTypeId = dimValueTypeMap.get(dim.dimensionValueId);
      if (!dimensionTypeId) continue;

      // Upsert dimension balance
      await tx.dimensionBalance.upsert({
        where: {
          uq_dimension_balance: {
            companyId,
            accountCode: line.accountCode,
            dimensionTypeId,
            dimensionValueId: dim.dimensionValueId,
            periodId,
          },
        },
        create: {
          companyId,
          accountCode: line.accountCode,
          dimensionTypeId,
          dimensionValueId: dim.dimensionValueId,
          periodId,
          totalDebit: line.debit,
          totalCredit: line.credit,
        },
        update: {
          totalDebit: { increment: line.debit },
          totalCredit: { increment: line.credit },
        },
      });
    }
  }
}

