// ---------------------------------------------------------------------------

export async function searchJournalEntries(
  prisma: PrismaClient,
  companyId: string,
  query: SearchJournalsQuery,
) {
  const { search, status, source, limit } = query;

  const where: Record<string, unknown> = {
    companyId,
    OR: [
      { entryNumber: { contains: search, mode: 'insensitive' } },
      { description: { contains: search, mode: 'insensitive' } },
      { reference: { contains: search, mode: 'insensitive' } },
    ],
  };

  if (status !== undefined) where.status = status;
  if (source !== undefined) where.source = source;

  const items = await prisma.journalEntry.findMany({
    where,
    take: limit,
    orderBy: [{ transactionDate: 'desc' }, { entryNumber: 'desc' }],
    select: LIST_SELECT,
  });

  return items.map((row) => normaliseListItem(row as unknown as Record<string, unknown>));
}
